const mongoose = require('mongoose');

const UserSchema = new mongoose.Schema({
    first_name: {
        type: String,
        required: true
    },
    last_name: {
        type: String,
        required: true
    },
    userName: {
        type: String,
        unique: true,
        required: true
    },
    password: {
        type: String,
        required: true
    },
    role: {
        type: String,
        enum: ["admin", "user"],
        default: "user"
    },
    following: [{
        type: mongoose.Schema.Types.ObjectId,
        ref: 'vacations',
        default: []
    }]
});

module.exports = mongoose.model('users', UserSchema);